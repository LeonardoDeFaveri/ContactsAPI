package utils.keys;

/**
 * Quest'interfaccia contiene tutte le chiavi che possono
 * essere usate per inserire i valori dei campi di un oggetto
 * di tipo Contact.
 */
public interface ContactKeys {
    public final String ID = "id";
    public final String FIRST_NAME = "firstName";
    public final String FAMILY_NAME = "familyName";
    public final String SECOND_NAME = "secondName";
    public final String OWNER = "owner";
    public final String ASSOCIATED_USER = "associatedUser";
    public final String EMAILS = "emails";
    public final String PHONE_NUMBERS = "phoneNumbers";
}